#include <iostream>

using namespace std;

int main() {
    int a = 13;
    int b = 4;
    
    // 정수형 변수 선언 및 산술 연산
    cout << a + b << endl; // 더하기
    cout << a - b << endl; // 빼기
    cout << a * b << endl; // 곱하기
    cout << a / b << endl; // 나누기
    cout << a % b << endl; // 모듈러 연산
    cout << -a << endl; // 부호 바꿈

    return 0;
}