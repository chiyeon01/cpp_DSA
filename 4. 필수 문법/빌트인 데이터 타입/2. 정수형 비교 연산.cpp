#include <iostream>

using namespace std;

int main() {
    int a = 13;
    int b = 4;

    // 정수형 비교 연산
    // 비교 연산이 참 -> 1, 거짓 -> 0 반환
    cout << (a == b) << endl;
    cout << (a != b) << endl;
    cout << (a > b) << endl;
    cout << (a < b) << endl;
    cout << (a >= b) << endl;
    cout << (a <= b) << endl;
    
    return 0;
}